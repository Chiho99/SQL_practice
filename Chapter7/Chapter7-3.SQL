-- 1
-- 飼育県別に飼育頭数をカウントした結果を、
-- 集計テーブルに登録する

INSERT INTO 頭数集計
    SELECT 飼育県, COUNT(個体識別番号)
    FROM 個体識別
    GROUP BY 飼育県;

-- 2
-- 1で作成して頭数集計テーブルで、飼育頭数の多い方から
-- 3つの都道府県で飼育されている牛のデータを、個体識別テーブル
-- より抽出する。抽出する項目は、都道府県名、個体識別番号、雄雌とする
-- ただし、雌雄コードではなく、「雌」、「雄」の日本語表記
SELECT 飼育県 AS 都道府県名, 個体識別番号,
    CASE 飼育コード WHEN '1' THEN '雄'
                   WHEN '2' THEN '雌'
    END AS 雄雌
    FROM 個体識別
    WHERE 飼育県 IN (SELECT 飼育県
                    FROM 頭数集計
                    ORDER BY 頭数 DESC
                    OFFSET 0 ROWS FETCH NEXT 3 ROWS ONLY)

-- 3
-- 個体識別テーブルには肉牛についてもデータ登録されており、
-- 母牛が乳用種である牛の一覧を個体識別テーブルにより抽出したい
-- 抽出する項目は、個体、識別番号、品種、出生日、母牛番号とする。
-- なお品種はコードではなく「乳用種」、「肉用種」、「交雑種」
SELECT 個体, 識別番号,
CASE 品種コード WHEN '01' THEN '乳用種'
               WHEN '02' THEN '肉用種'
               WHEN '03' THEN '交雑種'
END AS 品種, 出生日, 母牛番号
FROM 個体識別
WHERE 母牛番号 IN (SELECT 個体識別番号
                    FROM 個体識別
                    WHERE 品質コード = '01')